
import java.util.*;

/**
 * 
 */
public class VistaCronograma {

	private int dia;
	private Date fecha;
	 
	public VistaCronograma(int dia, Date fecha) {
		super();
		this.dia = dia;
		this.fecha = fecha;
	}

	public int getDia() {
		return dia;
	}

	public void setDia(int dia) {
		this.dia = dia;
	}

	public Date getFecha() {
		return fecha;
	}

	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}
	
}